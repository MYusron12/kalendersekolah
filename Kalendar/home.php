	<div class="two-thirds column omega">
		<h3>Gambaran Umum SMA Nusa Putra Tangerang : </h3>
		<p>
			  &nbsp;&nbsp;&nbsp;&nbsp;
              SMA NUSA PUTRA Tangerang merupakan lembaga pendidikan  kejuruan swasta. Sekolah ini berdiri berdiri pada tahun 1983 dengan akreditasi B, beralamat di Jl. Teuku Umar No.12, Kelularahan Nusa Jaya Kecamatan Karawaci Tangerang. Dartini,M.pd. Sebagai kepala sekolah, sekolah ini memiliki dua bidang yaitu Ilmu Pengetahuan Sosial (IPS), dan Ilmu Pengetahuan Alam (IPA). 
              <br/>
              &nbsp;&nbsp;&nbsp;&nbsp;
              Secara garis besar SMA NUSA PUTRA Tangerang memiliki tugas dan tanggung jawab yaitu sebagai pelaksana pendidikan dalam jangka waktu tertentu sesuai dengan jenis dan sifat sekolah tersebut. Melaksanakan pendidikan dan pengajaran sesuai dengan kewajiban yang berlaku, melaksanakan bimbingan dan penyuluhan bagi siswa-siswi di sekolah, membina Organisasi Siswa Intra Sekolah (OSIS), melaksanakan urusan Tata Usaha (TU), membina kerja sama dengan orang tua siswa,  masyarakat dan instansi terkait, melaksanakan program dinas pendidikan Kota, bertanggung jawab kepada Walikota melalui Kepala Dinas Pendidikan.
		</p>
