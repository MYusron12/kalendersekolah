    	</center>
<body class="nn"><p>SMA NUSA PUTRA Tangerang merupakan lembaga pendidikan  kejuruan swasta. </p>
<p>Sekolah ini berdiri berdiri pada tahun 1983 dengan akreditasi B, beralamat di Jl. Teuku Umar No.12, Kelularahan Nusa Jaya Kecamatan  Karawaci Tangerang. Dartini,M.pd. </p>
<p>Sebagai kepala sekolah, sekolah ini memiliki dua bidang yaitu <strong>Ilmu  Pengetahuan Sosial (IPS)</strong>, dan <strong>Ilmu Pengetahuan Alam (IPA).</strong> </p>
<p>Secara garis besar SMA NUSA PUTRA Tangerang memiliki tugas dan tanggung jawab yaitu  sebagai pelaksana pendidikan dalam jangka waktu tertentu sesuai dengan jenis  dan sifat sekolah tersebut.</p>
<p> Melaksanakan pendidikan dan pengajaran sesuai  dengan kewajiban yang berlaku, melaksanakan bimbingan dan penyuluhan bagi  siswa-siswi di sekolah, membina Organisasi Siswa Intra Sekolah (OSIS),  melaksanakan urusan Tata Usaha (TU), membina kerja sama dengan orang tua  siswa,  masyarakat dan instansi terkait,  melaksanakan program dinas pendidikan Kota, bertanggung jawab kepada Walikota  melalui Kepala Dinas Pendidikan.</p>
